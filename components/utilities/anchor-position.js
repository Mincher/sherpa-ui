/**
 * anchor-position.js — Shared positioning helper for floating elements
 * (tooltip, popover, menu, and any future components that anchor to a
 * trigger element).
 *
 * Usage:
 *   import { positionFloating } from '../utilities/anchor-position.js';
 *
 *   positionFloating(this, triggerEl, {
 *     position: 'bottom-start', // top|bottom|left|right with optional -start|-center|-end
 *     gap: 8,
 *     // sameTreeScopeOnly: when true (default), CSS anchor positioning is
 *     // only used when the floating element and anchor share a root node
 *     // (otherwise CSS anchor lookups silently fail across shadow roots).
 *     sameTreeScopeOnly: true,
 *   });
 *
 * The helper:
 *   1. Detects browser support for CSS anchor positioning.
 *   2. If supported AND the anchor is in the same tree scope, assigns a
 *      generated `anchor-name` to the trigger and sets `position-anchor`
 *      on the floating element. Visual placement is then driven by the
 *      component's CSS (`inset-area`, `position-area`, `top`, `left` …).
 *   3. Otherwise, falls back to absolute positioning calculated from
 *      `getBoundingClientRect()`.
 *
 * @module anchor-position
 */

const ANCHOR_PREFIX = '--sherpa-anchor-';

/** True if the browser supports CSS anchor positioning. */
export function supportsCSSAnchor() {
  return typeof CSS !== 'undefined' && CSS.supports?.('anchor-name: --test');
}

/**
 * Ensure the element has an anchor-name; return the name (with leading "--").
 * Marks the element so callers can clean up generated names if desired.
 * @param {HTMLElement} el
 * @returns {string}
 */
export function ensureAnchorName(el) {
  const existing = el.style.getPropertyValue('anchor-name').trim().split(/\s+/)[0];
  if (existing) return existing;
  const name = `${ANCHOR_PREFIX}${Math.random().toString(36).slice(2, 9)}`;
  el.style.setProperty('anchor-name', name);
  el.dataset.sherpaGeneratedAnchor = '';
  return name;
}

/**
 * Remove a previously generated anchor-name (no-op if not generated by us).
 * @param {HTMLElement} el
 */
export function clearGeneratedAnchorName(el) {
  if (!el || !('sherpaGeneratedAnchor' in el.dataset)) return;
  el.style.removeProperty('anchor-name');
  delete el.dataset.sherpaGeneratedAnchor;
}

/**
 * Position a floating element relative to an anchor.
 *
 * @param {HTMLElement} floating  The element to position (e.g. popover, menu).
 * @param {HTMLElement} anchor    The trigger element to anchor against.
 * @param {Object}      [opts]
 * @param {string}      [opts.position='bottom']  bottom | top | left | right
 *                                                with optional -start | -center | -end
 * @param {number}      [opts.gap=4]              Pixel gap between anchor and floating element.
 * @param {boolean}     [opts.sameTreeScopeOnly=true]
 *        When true (default), CSS anchor positioning is only used when
 *        anchor and floating share a tree scope. Cross-shadow-root anchor
 *        lookups fail silently in current browsers, so this falls back to
 *        rect-based positioning when the scopes differ.
 * @returns {{ mode: 'css-anchor' | 'rect', anchorName?: string }}
 *        Information about the strategy chosen.
 */
export function positionFloating(floating, anchor, opts = {}) {
  const {
    position = 'bottom',
    gap = 4,
    sameTreeScopeOnly = true,
  } = opts;

  const sameScope = anchor.getRootNode() === floating.getRootNode();
  const useCSS = supportsCSSAnchor() && (!sameTreeScopeOnly || sameScope);

  if (useCSS) {
    const anchorName = ensureAnchorName(anchor);
    floating.style.setProperty('position-anchor', anchorName);
    // Clear any rect-based inline coords so CSS rules win.
    floating.style.removeProperty('top');
    floating.style.removeProperty('left');
    floating.style.removeProperty('right');
    floating.style.removeProperty('bottom');
    floating.style.removeProperty('translate');
    return { mode: 'css-anchor', anchorName };
  }

  applyRectPosition(floating, anchor, position, gap);
  return { mode: 'rect' };
}

/**
 * Rect-based fallback positioning. Exposed for components that have
 * already detected the no-CSS-anchor case themselves.
 * @param {HTMLElement} floating
 * @param {HTMLElement} anchor
 * @param {string} position
 * @param {number} gap
 */
export function applyRectPosition(floating, anchor, position = 'bottom', gap = 4) {
  const rect = anchor.getBoundingClientRect();
  const [side, alignment = 'center'] = position.split('-');

  // Reset positional inline styles to avoid stale values.
  floating.style.removeProperty('top');
  floating.style.removeProperty('left');
  floating.style.removeProperty('right');
  floating.style.removeProperty('bottom');
  floating.style.removeProperty('translate');

  switch (side) {
    case 'top':
      floating.style.bottom = `${window.innerHeight - rect.top + gap}px`;
      applyHorizontalAlignment(floating, rect, alignment);
      break;
    case 'bottom':
      floating.style.top = `${rect.bottom + gap}px`;
      applyHorizontalAlignment(floating, rect, alignment);
      break;
    case 'left':
      floating.style.right = `${window.innerWidth - rect.left + gap}px`;
      applyVerticalAlignment(floating, rect, alignment);
      break;
    case 'right':
      floating.style.left = `${rect.right + gap}px`;
      applyVerticalAlignment(floating, rect, alignment);
      break;
    default:
      floating.style.top = `${rect.bottom + gap}px`;
      applyHorizontalAlignment(floating, rect, alignment);
  }
}

function applyHorizontalAlignment(floating, rect, alignment) {
  switch (alignment) {
    case 'start':
      floating.style.left = `${rect.left}px`;
      break;
    case 'end':
      floating.style.right = `${window.innerWidth - rect.right}px`;
      break;
    case 'center':
    default:
      floating.style.left = `${rect.left + rect.width / 2}px`;
      floating.style.translate = '-50% 0';
  }
}

function applyVerticalAlignment(floating, rect, alignment) {
  switch (alignment) {
    case 'start':
      floating.style.top = `${rect.top}px`;
      break;
    case 'end':
      floating.style.bottom = `${window.innerHeight - rect.bottom}px`;
      break;
    case 'center':
    default:
      floating.style.top = `${rect.top + rect.height / 2}px`;
      floating.style.translate = '0 -50%';
  }
}
